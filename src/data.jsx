export function signageArt(kind) {
  const o = C.orange;
  const p = C.paper;

  switch (kind) {
    case "grid":
      return (
        <>
          <rect x="60" y="60" width="280" height="100" fill="none" stroke={p} strokeWidth="2" />
          <rect x="170" y="160" width="60" height="40" fill="none" stroke={p} strokeWidth="2" />
        </>
      );
    case "lines":
      return (
        <>
          <line x1="0" y1="50" x2="400" y2="50" stroke={o} strokeWidth="0.6" opacity="0.5" />
          <line x1="0" y1="90" x2="400" y2="90" stroke={o} strokeWidth="0.6" opacity="0.5" />
          <line x1="0" y1="130" x2="400" y2="130" stroke={o} strokeWidth="0.6" opacity="0.5" />
          <rect x="80" y="60" width="240" height="100" fill="none" stroke={p} strokeWidth="2" />
        </>
      );
    case "panel":
      return (
        <>
          <rect x="50" y="40" width="300" height="40" fill={o} opacity="0.9" />
          <rect x="50" y="100" width="140" height="80" fill="none" stroke={p} strokeWidth="2" />
          <rect x="210" y="100" width="140" height="80" fill="none" stroke={p} strokeWidth="2" />
        </>
      );
    case "glow":
      return (
        <>
          <circle cx="200" cy="110" r="80" fill={o} opacity="0.18" />
          <circle cx="200" cy="110" r="50" fill={o} opacity="0.35" />
          <text x="130" y="120" fontFamily="Anton" fontSize="30" fill={p}>GLOW</text>
        </>
      );
    case "vehicle":
      return (
        <>
          <rect x="60" y="90" width="280" height="60" rx="8" fill="none" stroke={p} strokeWidth="2" />
          <circle cx="120" cy="160" r="16" fill={p} opacity="0.8" />
          <circle cx="300" cy="160" r="16" fill={p} opacity="0.8" />
          <rect x="90" y="100" width="220" height="30" fill={o} opacity="0.7" />
        </>
      );
    case "showroom":
      return (
        <>
          <rect x="40" y="50" width="100" height="120" fill="none" stroke={o} strokeWidth="2" />
          <rect x="160" y="50" width="200" height="60" fill={o} opacity="0.85" />
          <rect x="160" y="120" width="200" height="50" fill="none" stroke={p} strokeWidth="2" />
        </>
      );
    case "totem":
      return (
        <>
          <rect x="170" y="30" width="60" height="160" fill="none" stroke={o} strokeWidth="2" />
          <rect x="180" y="50" width="40" height="40" fill={o} opacity="0.7" />
        </>
      );
    case "led":
      return (
        <>
          <rect x="60" y="40" width="280" height="30" fill={o} opacity="0.2" />
          {[120, 160, 200, 240, 280].map((cx) => (
            <circle key={cx} cx={cx} cy="130" r="12" fill={o} />
          ))}
        </>
      );
    case "shopfront":
      return (
        <>
          <rect x="30" y="60" width="340" height="100" fill="none" stroke={p} strokeWidth="2" />
          <text x="60" y="125" fontFamily="Anton" fontSize="40" fill={o}>SHOP</text>
        </>
      );
    case "wayfind":
      return (
        <>
          <polygon points="40,180 200,40 360,180" fill="none" stroke={o} strokeWidth="2" />
          <rect x="170" y="120" width="60" height="60" fill={p} opacity="0.8" />
        </>
      );
    default:
      return null;
  }
}